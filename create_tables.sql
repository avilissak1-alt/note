ALTER TABLE grades ADD CONSTRAINT grades_week_check 
    CHECK (week >= 1 AND week <= 4);

-- Contrainte de validation pour les noms (non vides)
ALTER TABLE students ADD CONSTRAINT students_first_name_check 
    CHECK (length(trim(first_name)) > 0);

ALTER TABLE students ADD CONSTRAINT students_last_name_check 
    CHECK (length(trim(last_name)) > 0);

